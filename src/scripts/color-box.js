//List of functions
function randomColor() { //Used to generate random color
   return 'rgb(' + Math.floor(Math.random()*255) + ',' + Math.floor(Math.random()*255) + ',' + Math.floor(Math.random()*255) + ')';
}

function allBoxesFilled() { //Checks to see if boxes are full
  let allBoxesFilled = true;
  for (let index = 0; index < colorBoxes.length; index++) {
    if (colorBoxes.item(index).style.backgroundColor == "")
      allBoxesFilled = false;
  }
  return allBoxesFilled;
}

function bgChange(event) { //Changes background color
  if (event.target.className === 'colorBox' && event.target.style.backgroundColor === "") {
    if (colorSelect.value !== "") { //Chose color do this
      event.target.innerText = event.target.style.backgroundColor = colorSelect.value;
      colorSelect.value = false;
    } else { //Did not choose color do this
      event.target.innerText = event.target.style.backgroundColor = randomColor();
    }
    if (allBoxesFilled()){ //Once all boxes are full
      alert ("All Boxes have been filled!")
    }
  }
}

//Event listener
document.querySelector('body').addEventListener('click', bgChange);